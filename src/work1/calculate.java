package work1;

public class calculate {
	 public static void main(String[] args) {
		 
		  System.out.println(5 + 5);   // 數字相加，結果是 10
		  
		  System.out.println(5 + '5'); // 字元 '5' 轉成 ASCII 值是 53，5 + 53 = 58
		  
		  System.out.println(5 + "5"); // 數字加字串，結果是 "55"
		  
    }
}


